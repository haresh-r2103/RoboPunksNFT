const MainMint = ({ accounts, setAccounts }) => {
  const [mintAmount, setMintAmount] = useState(1);
  const isConnected = Boolean(accounts[0]);

  const handleMint = async () => {
    if (window.ethereum) {
      const provider = new ethers.BrowserProvider(window.ethereum);
      const signer = await provider.getSigner();
      const contractInstance = new ethers.Contract(
        roboPunksNFTAddress,
        ABI,
        signer
      );

      try {
  const response = await contractInstance.mint(BigInt(mintAmount), {
  value: ethers.parseEther((0.02 * mintAmount).toString()),
});

        console.log("response: ", response);
      } catch (error) {
        console.error("Error, ", error);
      }
    } else {
      alert("MetaMask wallet is not found!");
    }
  };

  const handleIncrement = () => {
    if (mintAmount >= 3) return;
    setMintAmount(mintAmount + 1);
  };

  const handleDecrement = () => {
    if (mintAmount <= 1) return;
    setMintAmount(mintAmount - 1);
  };

  return (
    <div className="mint-container">
      <h1 className="title">RoboPunks</h1>
      <p className="subtitle">
        It's 2078. Can the RoboPunks NFT save humans from destructive rampant
        NFT speculation? <br /> Mint RoboPunks to find out.
      </p>

      {isConnected ? (
        <div className="mint-box">
          <div className="mint-controls">
            <button className="counter-btn" onClick={handleDecrement}>
              -
            </button>
            <input
              type="number"
              className="mint-input"
              value={mintAmount}
              readOnly
            />
            <button className="counter-btn" onClick={handleIncrement}>
              +
            </button>
          </div>
          <button className="mint-btn" onClick={handleMint}>
            Mint Now
          </button>
        </div>
      ) : (
        <p className="connect-warning">⚡ You must be connected to Mint.</p>
      )}
    </div>
  );
};

export default MainMint;
